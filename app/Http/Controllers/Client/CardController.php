     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $authUser = auth()->user();
        $card = $authUser->paymentMethods()->toArray();
        if (\count($card) > 0) {
            $card[0]['card']['exp_year'] = substr($card[0]['card']['exp_year'], 2);
        }
        return Inertia::render('Card/Index', [
            'card' => $card
        ]);
    }

